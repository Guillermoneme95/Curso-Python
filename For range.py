#for range

for i in range(5):
    print(f"valor {i}")

'''No necesariamente tiene que ir de 0 a 4
puede ir en otros intervalos (5,10)'''

for g in range(5,10):
    print(f"valor {g}")

'''Tambien puede ir de a saltos'''

for d in range(5,50,3): #de 5 a 49 de 3 en 3
    print(f"valor {d}")




