#Operadores Aritmeticos (+ - * /)
#la division retorna siempre un valor flotante
num1 = 10
num2 = 3

resultado = num1 - num2
print(resultado)
#redondeo de division
nored = num1 / num2
red= num1 // num2
print(red)
print(nored)
#modulo (residuo de la division)
mod= num1 % num2
print (mod)
#exponenciacion
exp = num1 ** num2
print (exp)
